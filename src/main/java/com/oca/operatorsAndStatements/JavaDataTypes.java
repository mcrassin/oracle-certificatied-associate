public class JavaDataTypes {

    public static void main(String[] args) {
        System.out.println("byte:\t\t" + Byte.BYTES + " bytes.\t\t" + Byte.SIZE + " bits.");
        System.out.println();
        System.out.println("char:\t\t" + Character.BYTES + " bytes.\t\t" + Character.SIZE + " bits.");
        System.out.println("short:\t\t" + Short.BYTES + " bytes.\t\t" + Short.SIZE + " bits.");
        System.out.println();
        System.out.println("int:\t\t" + Integer.BYTES + " bytes.\t\t" + Integer.SIZE + " bits.");
        System.out.println("float:\t\t" + Float.BYTES + " bytes.\t\t" + Float.SIZE + " bits.");
        System.out.println();
        System.out.println("long:\t\t" + Long.BYTES + " bytes.\t\t" + Long.SIZE + " bits.");
        System.out.println("double:\t\t" + Double.BYTES + " bytes.\t\t" + Double.SIZE + " bits.");

    }

}
